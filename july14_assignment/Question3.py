#Given a list of numbers x = list(range(1, 20)).Loop over it using while loop and sum each numbers. Loop must terminate if the value of sum is greater than 34.

#x = list(range(1, 20))
x = list(range(1,20))
sum = 0
for each in x:

    sum+=each
    while sum<34:
      print(sum)
      break

















